public class OperadoresAtribuicao{

    public static void main(String[] args){
        int total = 10;       
        total += 3;      
        System.out.println(total); //imprimi 13
        total -= 2;
        System.out.println(total); //imprimi 11
        total *= 3;
        System.out.println(total);//imprimi 33
        total /= 3;
        System.out.println(total); //imprimi 11
        total %= 5;
        System.out.println(total); // imprimi 1
    }
}